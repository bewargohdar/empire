import 'package:empaire_film/features/movie/domain/entity/single_movie.dart';

class SingleMovieModel extends SingleMovieEntity {
  SingleMovieModel({
    required super.id,
    required super.title,
    required super.backdropPath,
    required super.overview,
    required super.releaseDate,
    required super.voteAverage,
    required super.voteCount,
    required super.originalLanguage,
    required super.originalTitle,
    required super.posterPath,
    required super.tagline,
    required super.status,
    required super.runtime,
    required super.budget,
    required super.revenue,
    required super.homepage,
    required super.imdbId,
    required super.productionCompanies,
    required super.productionCountries,
    required super.spokenLanguages,
    required super.genres,
    required super.adult,
    required super.belongsToCollection,
    required super.originCountry,
    required super.popularity,
    required super.video,
  });

  factory SingleMovieModel.fromJson(Map<String, dynamic> json) {
    return SingleMovieModel(
      id: json['id'],
      title: json['title'],
      backdropPath: json['backdrop_path'],
      overview: json['overview'],
      releaseDate: json['release_date'],
      voteAverage: json['vote_average'],
      voteCount: json['vote_count'],
      originalLanguage: json['original_language'],
      originalTitle: json['original_title'],
      posterPath: json['poster_path'],
      tagline: json['tagline'],
      status: json['status'],
      runtime: json['runtime'],
      budget: json['budget'],
      revenue: json['revenue'],
      homepage: json['homepage'],
      imdbId: json['imdb_id'],
      productionCompanies: json['production_companies'],
      productionCountries: json['production_countries'],
      spokenLanguages: json['spoken_languages'],
      genres: json['genres'],
      adult: json['adult'],
      belongsToCollection: json['belongs_to_collection'],
      originCountry: json['origin_country'],
      popularity: json['popularity'],
      video: json['video'],
    );
  }
  Map<String, dynamic> toMap() {
    return {
      'id': id,
      'title': title,
      'backdropPath': backdropPath,
      'overview': overview,
      'releaseDate': releaseDate,
      'voteAverage': voteAverage,
      'voteCount': voteCount,
      'originalLanguage': originalLanguage,
      'originalTitle': originalTitle,
      'posterPath': posterPath,
      'tagline': tagline,
      'status': status,
      'runtime': runtime,
      'budget': budget,
      'revenue': revenue,
      'homepage': homepage,
      'imdbId': imdbId,
      'productionCompanies': productionCompanies,
      'productionCountries': productionCountries,
      'spokenLanguages': spokenLanguages,
      'genres': genres,
      'adult': adult,
      'belongsToCollection': belongsToCollection,
      'originCountry': originCountry,
      'popularity': popularity,
      'video': video,
    };
  }
}
